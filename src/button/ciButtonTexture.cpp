#include "ciButtonTexture.h"

using namespace std;
using namespace ci;
using namespace ci::app;


namespace coc {


void ciButtonTexture::setTexture( ci::gl::TextureRef _tex )
{
	tex = _tex;
}

void ciButtonTexture::draw() const
{
	if (tex) {
		gl::draw( tex, rect );
	}
	else {
		ciButton::draw();
	}
}


}
